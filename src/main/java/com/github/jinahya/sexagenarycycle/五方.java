/**
 * Constants for five cardinal directions.
 *
 * @author Jin Kwon &lt;onacit_at_gmail.com&gt;
 * @see 五方正色#valueOf(五方)
 * @see 天干#五方
 */
@SuppressWarnings({"NonAsciiCharacters", "java:S115"})
public enum 五方 {

    /**
     * 0: East.
     */
    東, // 동녘 동

    /**
     * 1: South.
     */
    南, // 남녘 남

    /**
     * 3: Center.
     */
    中, // 가운데 중

    /**
     * 4: West.
     */
    西, // 남녘 남

    /**
     * 5: North.
     */
    北; // 북녘 북
}
